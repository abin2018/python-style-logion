def who_dreamed_a_dream(Zhuangzhou, Butterfly):
	"""
	不知周之梦为胡蝶与，胡蝶之梦为周与？周与胡蝶，则必有分矣。此之谓物化。 -- 《庄子·齐物论》
	"""
	Zhuangzhou.magically_change_to = Butterfly
	Butterfly.magically_change_to = Zhuangzhou
	return Zhuangzhou != Butterfly

